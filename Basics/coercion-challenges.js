// Coercion examples
let result1 = true + 1; // true converts to 1
console.log("Result 1 (true + 1):", result1); // Output: 2

let result2 = "5" - 3; // "5" converts to number 5
console.log("Result 2 ('5' - 3):", result2); // Output: 2

let result3 = 10 + " apples"; // 10 converts to a string
console.log("Result 3 (10 + ' apples'):", result3); // Output: "10 apples"
