// Declare variables
let num = "25"; // String
console.log("Before Conversion:", num, ", Type:", typeof num);

// Convert to a number
num = Number(num);
console.log("After Conversion:", num, ", Type:", typeof num);

// Declare a boolean
let isAvailable = true; // Boolean
console.log("Before Conversion:", isAvailable, ", Type:", typeof isAvailable);

// Convert to a string
isAvailable = String(isAvailable);
console.log("After Conversion:", isAvailable, ", Type:", typeof isAvailable);
